// program to display text 5 times
const n = 5;

// looping from i = 1 to 5
// in each iteration, i is increased by 1
for (let i = 1; i <= n; i++) {
    /*The loop first delcares a variable.
    Then, it states a condition like if i is less than or equal to n.
    Finally, it states what will happen if the condition is true.*/
    console.log(i);
    //This will display all numbers from 1 to 5.
}



// program to display the sum of natural numbers
let sum = 0;
const num = 100

// looping from i = 1 to n
// in each iteration, i is increased by 1
for (let i = 1; i <= num; i++) {
    sum += i;  // sum = sum + i
}

console.log('sum:', sum);



